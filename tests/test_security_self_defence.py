class TestUnicodeTricks(unittest.TestCase):
    """Test that Unicode tricks don't confuse credential detection.

    Attack: Credentials embedded using Unicode lookalikes, zero-width
    characters, or right-to-left override to evade regex patterns.
    These are mass-attack viable because the payloads are simple to
    generate.
    """

    def test_zero_width_chars_in_key(self):
        """AKIA key with zero-width spaces inserted should still be caught.

        Note: This tests current behaviour. Zero-width chars WILL break
        regex matching — this is a known limitation. The test documents it.
        If the key is split by zero-width chars, it won't match.
        """
        check = CredentialExposureCheck(_make_config())
        # Zero-width space (U+200B) inserted between characters
        zwsp = "\u200b"
        obfuscated = f"AK{zwsp}IA{zwsp}IOSFODNN7EXAMPLE"
        findings = check._scan_value(obfuscated)
        # Current regex WON'T match this — document as known limitation
        # The entropy scanner in DLP-Lite is the fallback for these cases
        # This test just confirms the regex doesn't CRASH on these inputs
        # No assertion on findings — we're testing for crashes, not detection

    def test_right_to_left_override(self):
        """RTL override character should not crash scanner."""
        check = CredentialExposureCheck(_make_config())
