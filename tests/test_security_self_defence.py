# 1. ReDoS — Regex Denial of Service
# =========================================================================

class TestReDoSCredentialPatterns(unittest.TestCase):
    """Verify all 14 credential patterns resist catastrophic backtracking.

    Attack: An adversary crafts a tool parameter string designed to make
    a regex pattern backtrack exponentially. This hangs the enforcement
    pipeline, causing a denial of service that lets subsequent tool calls
    through unscanned (if the sidecar times out and the adapter falls
    back to allow).

    These inputs are generated by standard ReDoS fuzzing tools (e.g.,
    regexploit, rxxr2) and circulate freely on GitHub.
    """

    def _time_pattern(self, pattern: re.Pattern, evil_input: str) -> float:
        """Time how long a pattern takes to match against input."""
        start = time.monotonic()
        pattern.findall(evil_input)
        elapsed = time.monotonic() - start
        return elapsed

    def test_aws_key_pattern_no_redos(self):
        """AKIA pattern: fixed-length, no backtracking risk."""
        pattern = CREDENTIAL_PATTERNS[0]  # AKIA[0-9A-Z]{16}
        # Near-miss: AKIA followed by 15 valid chars + 1 invalid, repeated
        evil = "AKIA" + "A" * 15 + "!" * 1000
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"AWS key pattern took {elapsed:.3f}s on adversarial input")

    def test_aws_secret_pattern_no_redos(self):
        """AWS secret pattern: \\S{20,} could backtrack on near-misses."""
        pattern = CREDENTIAL_PATTERNS[1]
        evil = "aws_secret_access_key=" + "a" * 10000
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"AWS secret pattern took {elapsed:.3f}s")

    def test_generic_api_key_pattern_no_redos(self):
        """Generic api_key pattern has optional groups — test for backtracking."""
        pattern = CREDENTIAL_PATTERNS[8]  # api[_-]?key...
        # Adversarial: api_key= followed by long string of chars that
        # partially match [A-Za-z0-9_\\-]{20,} then fail
        evil = "api_key=" + "a" * 5000 + "\x00"
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"Generic API key pattern took {elapsed:.3f}s")

    def test_jwt_pattern_no_redos(self):
        """JWT pattern has three dot-separated groups — potential backtrack point."""
        pattern = CREDENTIAL_PATTERNS[9]  # eyJ...\\.eyJ...\\.[...]
        # Near-valid JWT with many dots to confuse the parser
        evil = "eyJ" + "a" * 100 + "." * 500 + "eyJ" + "b" * 100
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"JWT pattern took {elapsed:.3f}s on adversarial input")

    def test_bearer_token_pattern_no_redos(self):
        """Bearer token pattern: \\S{20,} class."""
        pattern = CREDENTIAL_PATTERNS[11]  # bearer/token...
        evil = "bearer=" + "a.b-c_" * 2000
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"Bearer pattern took {elapsed:.3f}s")

    def test_password_pattern_no_redos(self):
        """Password pattern: \\S{8,} could match greedily then fail."""
        pattern = CREDENTIAL_PATTERNS[12]  # password/passwd/pwd
        evil = "password=" + "x" * 10000
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"Password pattern took {elapsed:.3f}s")

    def test_connection_string_pattern_no_redos(self):
        """Connection string: \\S+:\\S+@ has two greedy quantifiers."""
        pattern = CREDENTIAL_PATTERNS[13]  # postgres://...
        # Adversarial: long username/password with no @ terminator
        evil = "postgres://" + "a" * 5000 + ":" + "b" * 5000
        elapsed = self._time_pattern(pattern, evil)
        self.assertLess(elapsed, REGEX_TIME_LIMIT,
                        f"Connection string pattern took {elapsed:.3f}s")

    def test_all_patterns_against_random_noise(self):
        """Run all patterns against 100KB of random-ish characters.

        This catches patterns that degrade on large non-matching input.
        """
        import string
        noise = (string.ascii_letters + string.digits + "._-=:/ ") * 1000
        for i, pattern in enumerate(CREDENTIAL_PATTERNS):
            elapsed = self._time_pattern(pattern, noise)
            self.assertLess(elapsed, REGEX_TIME_LIMIT,
                            f"Pattern {i} ({pattern.pattern[:30]}) took "
                            f"{elapsed:.3f}s on noise input")

    def test_all_patterns_against_repeated_near_match(self):
        """Feed each pattern a string that almost matches, repeated 1000 times.

        This is the classic ReDoS trigger — a near-match that forces
        the regex engine to explore many paths before failing.
        """
        near_misses = [
            "AKIA" + "A" * 15 + "!",                              # AWS key
            "aws_secret_access_key= " + "a" * 19 + "!",           # AWS secret
            "AIza" + "A" * 34 + "!",                               # GCP
            "sk_live_" + "a" * 19 + "!",                           # Stripe
            "ghp_" + "A" * 35 + "!",                               # GitHub
            "github_pat_" + "A" * 21 + "!",                        # GitHub PAT
            "sk-" + "A" * 19 + "!",                                # OpenAI
            "sk-ant-" + "A" * 19 + "!",                            # Anthropic
            "api_key=" + "A" * 19 + "!",                           # Generic
            "eyJ" + "A" * 9 + "." + "eyJ" + "A" * 9 + "." + "!", # JWT
            "-----BEGIN PRIVATE KEY---",                            # PEM (truncated)
            "bearer=" + "A" * 19 + "!",                            # Bearer
            "password=" + "A" * 7 + "!",                           # Password
            "postgres://a:b",                                       # Connection (no @)
            "gw_tok_" + "A" * 19 + "!",                            # OpenClaw
        ]
        for i, near_miss in enumerate(near_misses):
            if i >= len(CREDENTIAL_PATTERNS):
                break
            evil = (near_miss + " ") * 1000
            elapsed = self._time_pattern(CREDENTIAL_PATTERNS[i], evil)
            self.assertLess(elapsed, REGEX_TIME_LIMIT,
                            f"Pattern {i} took {elapsed:.3f}s on repeated near-miss")


