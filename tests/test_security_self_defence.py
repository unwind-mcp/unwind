def _make_config() -> UnwindConfig:
    """Build a minimal config for testing."""
    import tempfile
    tmp = tempfile.mkdtemp()
    config = UnwindConfig(
        unwind_home=Path(tmp) / ".unwind",
        workspace_root=Path(tmp) / "workspace",
    )
    config.ensure_dirs()
    (Path(tmp) / "workspace").mkdir(exist_ok=True)
    return config


# Max time in seconds any single regex match should take.
# If a pattern takes longer than this on adversarial input, it's vulnerable.
REGEX_TIME_LIMIT = 0.1  # 100ms — generous; most should be <1ms


