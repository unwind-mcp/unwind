class TestSidecarMalformedInput(unittest.TestCase):
    """Fuzz the sidecar server with malformed HTTP requests.

    Attack: A malicious MCP server or compromised adapter sends garbage
    to the sidecar's /v1/policy/check endpoint, hoping to crash it or
    bypass the fail-closed contract.
    """

    TEST_SECRET = "self-defence-test-secret"

    @classmethod
    def setUpClass(cls):
        """Create a test client for the sidecar with mandatory auth."""
        from unwind.sidecar.server import create_app
        from starlette.testclient import TestClient
        cls.app = create_app(shared_secret=cls.TEST_SECRET)
        cls.client = TestClient(cls.app)
        cls.headers = {
            "Authorization": f"Bearer {cls.TEST_SECRET}",
            "X-UNWIND-API-Version": "1",
        }

    def _post_policy(self, body=None, raw_body=None):
        """Send a POST to /v1/policy/check."""
        if raw_body is not None:
            return self.client.post(
                "/v1/policy/check",
                content=raw_body,
                headers={**self.headers, "Content-Type": "application/json"},
            )
        return self.client.post(
            "/v1/policy/check",
            json=body,
            headers=self.headers,
        )

    def test_empty_body(self):
        """Empty POST body should return 422, NOT crash."""
        resp = self._post_policy(raw_body=b"")
        self.assertIn(resp.status_code, (422, 400))

    def test_null_json(self):
        """JSON null body should return 422."""
        resp = self._post_policy(raw_body=b"null")
        self.assertIn(resp.status_code, (422, 400))

    def test_json_array_instead_of_object(self):
        """JSON array body should return 422."""
        resp = self._post_policy(body=[1, 2, 3])
        self.assertEqual(resp.status_code, 422)

    def test_json_string_instead_of_object(self):
        """JSON string body should return 422."""
        resp = self._post_policy(raw_body=b'"hello"')
        self.assertEqual(resp.status_code, 422)

    def test_deeply_nested_json(self):
        """500-level deep JSON nesting should not crash the server."""
        # Build nested dict: {"a": {"a": {"a": ... }}}
        inner = {"toolName": "test", "params": {}, "agentId": "a", "sessionKey": "s"}
        for _ in range(200):
            inner = {"nested": inner}
        resp = self._post_policy(body=inner)
        # Should get 422 (missing required fields at top level) or 200 with block
        self.assertIn(resp.status_code, (200, 422))

    def test_huge_tool_name(self):
        """1MB tool name should be rejected, not processed."""
        body = {
            "toolName": "x" * (1024 * 1024),
            "params": {},
            "agentId": "agent1",
            "sessionKey": "sess1",
        }
        resp = self._post_policy(body=body)
        # Should be 422 (exceeds max length) — NOT a hang
        self.assertEqual(resp.status_code, 422)

    def test_huge_params_value(self):
        """10MB string in params should be handled within time limit."""
        body = {
            "toolName": "fs_read",
            "params": {"path": "x" * (10 * 1024 * 1024)},
            "agentId": "agent1",
            "sessionKey": "sess1",
        }
        start = time.monotonic()
        resp = self._post_policy(body=body)
        elapsed = time.monotonic() - start
        self.assertIn(resp.status_code, (200, 422))
        self.assertLess(elapsed, 5.0,
                        f"10MB params took {elapsed:.1f}s — potential DoS")

    def test_many_params_keys(self):
        """10,000 parameter keys should not hang the recursive scanner."""
        body = {
            "toolName": "http_post",
            "params": {f"key_{i}": f"value_{i}" for i in range(10000)},
            "agentId": "agent1",
            "sessionKey": "sess1",
        }
        start = time.monotonic()
        resp = self._post_policy(body=body)
        elapsed = time.monotonic() - start
        self.assertIn(resp.status_code, (200,))
        self.assertLess(elapsed, 5.0,
                        f"10K params took {elapsed:.1f}s — potential DoS")

    def test_binary_garbage(self):
        """Random bytes should return 422, not crash."""
        import os
        garbage = os.urandom(1024)
        resp = self._post_policy(raw_body=garbage)
        self.assertIn(resp.status_code, (422, 400))

    def test_truncated_json(self):
        """Truncated JSON should return 422."""
        resp = self._post_policy(raw_body=b'{"toolName": "test", "par')
        self.assertIn(resp.status_code, (422, 400))

    def test_unicode_bomb(self):
        """Repeated Unicode combining characters in tool name."""
        body = {
            "toolName": "a" + "\u0300" * 10000,  # 'a' + 10K combining accents
            "params": {},
            "agentId": "agent1",
            "sessionKey": "sess1",
        }
        resp = self._post_policy(body=body)
        # Should be 422 (exceeds max length when expanded) or 200
        self.assertIn(resp.status_code, (200, 422))

    def test_null_bytes_in_params(self):
        """Null bytes in parameter values should not crash regex scanning."""
        body = {
            "toolName": "http_post",
            "params": {"body": "hello\x00world\x00AKIAIOSFODNN7EXAMPLE"},
            "agentId": "agent1",
            "sessionKey": "sess1",
        }
        resp = self._post_policy(body=body)
        self.assertEqual(resp.status_code, 200)


