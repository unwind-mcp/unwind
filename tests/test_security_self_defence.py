# 5. Parameter Depth Bombs
# =========================================================================

class TestParameterDepthBombs(unittest.TestCase):
    """Test that deeply nested parameters don't cause stack overflow.

    Attack: A tool call with deeply nested dicts (1000+ levels) that
    would cause a stack overflow in the recursive scanner if there's
    no depth limit.
    """

    def test_depth_100_no_crash(self):
        """100-level nesting should not crash."""
        check = CredentialExposureCheck(_make_config())
        inner = {"key": "AKIAIOSFODNN7EXAMPLE"}
        for _ in range(100):
            inner = {"nested": inner}
        # Should not raise RecursionError
        findings = check._scan_params_recursive(inner)
        # Credential is at depth 101, beyond max_depth=5, so not found
        self.assertEqual(len(findings), 0)

    def test_depth_1000_no_crash(self):
        """1000-level nesting should not crash (max_depth stops it)."""
        check = CredentialExposureCheck(_make_config())
        inner = {"key": "AKIAIOSFODNN7EXAMPLE"}
        for _ in range(1000):
            inner = {"nested": inner}
        findings = check._scan_params_recursive(inner)
        self.assertEqual(len(findings), 0)

    def test_wide_and_deep(self):
        """50 keys per level, 5 levels deep — tests MAX_SCAN_ITEMS cap.

        Without the cap, 50^5 = 312M leaf nodes would OOM the process.
        The cap (10,000 items) should trigger quickly and report the limit.
        """
        check = CredentialExposureCheck(_make_config())

        def build_wide(depth):
            if depth <= 0:
                return {"val": "clean_text_no_creds"}
            return {f"k{i}": build_wide(depth - 1) for i in range(50)}

        params = build_wide(4)  # 50^4 = 6.25M — well above cap
        start = time.monotonic()
        findings = check._scan_params_recursive(params)
        elapsed = time.monotonic() - start
        self.assertLess(elapsed, 5.0,
                        f"Wide+deep scan took {elapsed:.1f}s")
        # Should have hit the scan limit
        self.assertTrue(
            any("SCAN_LIMIT_REACHED" in f for f in findings),
            "Wide+deep bomb should trigger SCAN_LIMIT_REACHED"
        )


